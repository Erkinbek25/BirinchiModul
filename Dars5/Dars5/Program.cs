        static List<int> IncreaseOddNums(List<int> texts)
        {
            int lastodd = 0;
            foreach (var num in texts)
            {
                if (num % 2 != 0)
                {
                    lastodd = num;

                }
            }
            for (var i = 0; i < texts.Count; i++)
            {
                if (texts[i] % 2 != 0)
                {
                    texts[i] += lastodd;
                }
            }
            return texts;
        }

        static List<int> IncreaseEvenNums(List<int> texts)
        {
            int firsteven = 0;
            foreach (var num in texts)
            {
                if (num % 2 == 0)
                {
                    firsteven = num;
                    break;
                }
            }
            for (var i = 0; i < texts.Count; i++)
            {
                if (texts[i] % 2 == 0)
                {
                    texts[i] += firsteven;
                }
            }
            return texts;
        }

