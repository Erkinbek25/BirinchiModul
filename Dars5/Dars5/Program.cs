        static List<int> MaxList( List<int> numsa, List<int> numsb)
        {
            List<int> maxList = new List<int>();
            for ( var i = 0; i < numsa.Count; i++)
            {
                maxList.Add(Math.Max(numsa[i], numsb[i]));
            }
            return maxList;
        }


