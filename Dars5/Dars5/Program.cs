        static bool CompareOddandEvenNums(List<int> nums)
        {
            var OddCounter = 0;
            var EvenCounter = 0;

            foreach (var num in nums)
            {
                if (num % 2 == 0)
                {
                    ++EvenCounter;
                }
                else
                {
                    ++OddCounter;
                }

            }

            return OddCounter > EvenCounter;
        }


